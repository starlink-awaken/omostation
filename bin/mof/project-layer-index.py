#!/usr/bin/env python3
"""Generate the project layer index digest from docs/project-registry.yaml."""
from __future__ import annotations

import argparse
import sys
from pathlib import Path
from typing import Any

import yaml


WORKSPACE = Path(__file__).resolve().parents[2]
REGISTRY = WORKSPACE / "docs/project-registry.yaml"
OUTPUT = WORKSPACE / "docs/generated/project-layer-index.md"
LAYER_ORDER = ["L4", "L3", "I0", "L2", "L1", "L0", "M0", "X"]


def load_registry() -> dict[str, Any]:
    return yaml.safe_load(REGISTRY.read_text(encoding="utf-8")) or {}


def render_digest(registry: dict[str, Any]) -> str:
    layers = registry.get("layers", {})
    projects = registry.get("projects", {})
    by_layer: dict[str, list[str]] = {layer: [] for layer in LAYER_ORDER}
    for name, meta in sorted(projects.items()):
        if not isinstance(meta, dict):
            continue
        layer = str(meta.get("layer", ""))
        by_layer.setdefault(layer, []).append(name)

    lines = [
        "# Project Layer Index Digest",
        "",
        "<!-- AUTO-GENERATED by bin/mof/project-layer-index.py — do not edit manually -->",
        "",
        "> SSOT: `docs/project-registry.yaml::layers/projects`",
        "> Regenerate: `python3 bin/mof/project-layer-index.py --write`",
        "",
        "| Layer | Role | Projects |",
        "|-------|------|----------|",
    ]
    for layer in [*LAYER_ORDER, *sorted(set(by_layer) - set(LAYER_ORDER))]:
        role = layers.get(layer, "")
        names = by_layer.get(layer, [])
        project_text = ", ".join(f"`{name}`" for name in names) if names else "-"
        lines.append(f"| {layer} | {role} | {project_text} |")
    lines.append("")
    return "\n".join(lines)


def main() -> int:
    parser = argparse.ArgumentParser(description="Generate project layer index digest")
    parser.add_argument("--write", action="store_true", help="Write the generated digest")
    parser.add_argument("--check", action="store_true", help="Fail if the generated digest is stale")
    args = parser.parse_args()

    digest = render_digest(load_registry())
    if args.write:
        OUTPUT.parent.mkdir(parents=True, exist_ok=True)
        OUTPUT.write_text(digest + "\n", encoding="utf-8")
        print(f"Updated {OUTPUT.relative_to(WORKSPACE)}")
        return 0
    if args.check:
        current = OUTPUT.read_text(encoding="utf-8") if OUTPUT.exists() else ""
        expected = digest + "\n"
        if current != expected:
            print(f"stale generated digest: {OUTPUT.relative_to(WORKSPACE)}", file=sys.stderr)
            print("run: python3 bin/mof/project-layer-index.py --write", file=sys.stderr)
            return 1
        print(f"fresh: {OUTPUT.relative_to(WORKSPACE)}")
        return 0
    print(digest)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

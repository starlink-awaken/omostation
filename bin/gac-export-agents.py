#!/usr/bin/env python3
"""gac-export-agents.py — 从 governance-checks.yaml 导出 GaC 规则速览到 AGENTS.md

T1.1: AGENTS.md GaC 段导出 (GaC Stage 1 执行绑定)
读取注册表 → 按维度+层分组 → 输出 markdown 表格 → 追加到 AGENTS.md
"""
import sys
from pathlib import Path

import yaml

WORKSPACE = Path(__file__).resolve().parent.parent
REGISTRY = WORKSPACE / ".omo/_truth/registry/governance-checks.yaml"
AGENTS_MD = WORKSPACE / "AGENTS.md"

# Markers for insertion
START_MARKER = "<!-- GaC-RULES-START -->"
END_MARKER = "<!-- GaC-RULES-END -->"


def load_rules():
    with open(REGISTRY) as f:
        docs = list(yaml.safe_load_all(f))
    for doc in docs:
        if doc and isinstance(doc, dict) and "gac" in doc:
            return doc["gac"].get("rules", [])
    return []


def export_markdown(rules):
    by_dim = {}
    for r in rules:
        dim = r.get("dimension", "?")
        by_dim.setdefault(dim, []).append(r)

    lines = [START_MARKER]
    lines.append("<!-- AUTO-GENERATED by bin/gac-export-agents.py — do not edit manually -->")
    lines.append(
        f"<!-- Run: python3 bin/gac-export-agents.py to regenerate. Source: governance-checks.yaml ({len(rules)} rules) -->"
    )
    lines.append("")
    lines.append(f"### GaC 规则速览 ({len(rules)} 条)")
    lines.append("")
    lines.append("> SSOT: `.omo/_truth/registry/governance-checks.yaml::gac.rules`")
    lines.append("> 校验: `python3 bin/gac-validate.py --gate` | 漂移: `python3 bin/gac-drift.py`")
    lines.append("")

    for dim in ["X1", "X2", "X3", "X4"]:
        dim_rules = by_dim.get(dim, [])
        if not dim_rules:
            continue
        dim_names = {"X1": "X1 审计", "X2": "X2 抗熵", "X3": "X3 价值", "X4": "X4 一致性"}
        lines.append(f"#### {dim_names.get(dim, dim)} ({len(dim_rules)} 条)")
        lines.append("")
        lines.append("| 规则 ID | 层 | 检查类型 | 描述 |")
        lines.append("|---------|:--:|---------|------|")
        for r in sorted(dim_rules, key=lambda x: x.get("id", "")):
            rid = r.get("id", "")
            layer = r.get("layer", "?")
            check_type = r.get("check_type", "?")
            desc = r.get("description", "")[:60]
            lines.append(f"| `{rid}` | {layer} | {check_type} | {desc} |")
        lines.append("")

    lines.append(END_MARKER)
    return "\n".join(lines)


def update_agents_md(gac_section):
    content = AGENTS_MD.read_text()

    if START_MARKER in content and END_MARKER in content:
        # Replace existing section
        start_idx = content.index(START_MARKER)
        end_idx = content.index(END_MARKER) + len(END_MARKER)
        new_content = content[:start_idx] + gac_section + content[end_idx:]
    else:
        # Append at end
        new_content = content.rstrip() + "\n\n" + gac_section + "\n"

    AGENTS_MD.write_text(new_content)
    print(f"✅ Updated {AGENTS_MD.name} with {len(load_rules())} GaC rules")


def main():
    rules = load_rules()
    if not rules:
        print("❌ No GaC rules found in registry", file=sys.stderr)
        sys.exit(1)

    print(f"Loaded {len(rules)} GaC rules from registry")
    gac_md = export_markdown(rules)

    if "--stdout" in sys.argv:
        print(gac_md)
    else:
        update_agents_md(gac_md)


if __name__ == "__main__":
    main()

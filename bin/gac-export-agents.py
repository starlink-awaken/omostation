#!/usr/bin/env python3
"""gac-export-agents.py — 从 governance-checks.yaml 导出 agent-facing GaC 摘要.

T1.1: AGENTS.md GaC 段导出 (GaC Stage 1 执行绑定)
读取注册表 → 生成完整规则 digest → AGENTS.md 只保留 SSOT 指针
"""
from __future__ import annotations

import sys
from pathlib import Path

import yaml

WORKSPACE = Path(__file__).resolve().parent.parent
REGISTRY = WORKSPACE / ".omo/_truth/registry/governance-checks.yaml"
AGENTS_MD = WORKSPACE / "AGENTS.md"
GENERATED_DIR = WORKSPACE / "docs/generated"
GENERATED_RULES_MD = GENERATED_DIR / "agent-gac-rules.md"

# Markers for insertion
START_MARKER = "<!-- GaC-RULES-START -->"
END_MARKER = "<!-- GaC-RULES-END -->"


def load_rules():
    with open(REGISTRY) as f:
        docs = list(yaml.safe_load_all(f))
    for doc in docs:
        if doc and isinstance(doc, dict) and "gac" in doc:
            return doc["gac"].get("rules", [])
    return []


def load_source_descriptions():
    """从 source_ref 拉取 legacy_index 规则的描述.

    legacy_index 规则在 governance-checks.yaml 中 description 为空,
    实际描述在 source_ref 指向的源文件中.
    源文件使用不同的 ID 字段名 (id / rule_id) 和描述字段名 (description / title / name).
    """
    source_map = {}

    source_files = [
        WORKSPACE / ".omo/_truth/x1-governance-policies.yaml",
        WORKSPACE / ".omo/_truth/x2-freshness-rules.yaml",
        WORKSPACE / ".omo/_truth/x4-consistency-rules.yaml",
        WORKSPACE / "projects/ecos/src/ecos/ssot/registry/L0-constraints.yaml",
    ]

    for sf_path in source_files:
        if not sf_path.exists():
            continue
        with open(sf_path) as f:
            docs = [d for d in yaml.safe_load_all(f) if d]
        for doc in docs:
            if not isinstance(doc, dict):
                continue
            for _key, val in doc.items():
                if not isinstance(val, list):
                    continue
                for item in val:
                    if not isinstance(item, dict):
                        continue
                    rid = item.get("id", "") or item.get("rule_id", "") or item.get("policy_id", "")
                    desc = item.get("description", "") or item.get("title", "") or item.get("name", "") or item.get("desc", "")
                    if rid and desc:
                        source_map[rid] = desc

    return source_map


def export_full_markdown(rules):
    source_descs = load_source_descriptions()

    by_dim = {}
    for r in rules:
        dim = r.get("dimension", "?")
        by_dim.setdefault(dim, []).append(r)

    lines = ["# Agent GaC Rules Digest", ""]
    lines.append("<!-- AUTO-GENERATED by bin/gac-export-agents.py — do not edit manually -->")
    lines.append(
        f"<!-- Run: python3 bin/gac-export-agents.py to regenerate. Source: governance-checks.yaml ({len(rules)} rules) -->"
    )
    lines.append("")
    lines.append(f"### GaC 规则速览 ({len(rules)} 条)")
    lines.append("")
    lines.append("> SSOT: `.omo/_truth/registry/governance-checks.yaml::gac.rules`")
    lines.append("> 校验: `python3 bin/gac-validate.py --gate` | 漂移: `python3 bin/gac-drift.py`")
    lines.append("> legacy_index 规则描述从 source_ref 源文件拉取 (x1-policies / x2-freshness / x4-consistency / L0-constraints)")
    lines.append("")

    for dim in ["X1", "X2", "X3", "X4"]:
        dim_rules = by_dim.get(dim, [])
        if not dim_rules:
            continue
        dim_names = {"X1": "X1 审计", "X2": "X2 抗熵", "X3": "X3 价值", "X4": "X4 一致性"}
        lines.append(f"#### {dim_names.get(dim, dim)} ({len(dim_rules)} 条)")
        lines.append("")
        lines.append("| 规则 ID | 层 | 检查类型 | 描述 |")
        lines.append("|---------|:--:|---------|------|")
        for r in sorted(dim_rules, key=lambda x: x.get("id", "")):
            rid = r.get("id", "")
            layer = r.get("layer", "?")
            check_type = r.get("check_type", "?")
            # Use source_ref description if registry description is empty
            desc = r.get("description", "")
            if not desc.strip() and rid in source_descs:
                desc = source_descs[rid]
            # Truncate to 120 chars (was 60, caused truncation)
            if len(desc) > 120:
                desc = desc[:117] + "..."
            lines.append(f"| `{rid}` | {layer} | {check_type} | {desc} |")
        lines.append("")

    return "\n".join(lines)


def export_agents_pointer() -> str:
    lines = [START_MARKER]
    lines.append("<!-- AUTO-GENERATED by bin/gac-export-agents.py — do not edit manually -->")
    lines.append("")
    lines.append("### GaC Rules Pointer")
    lines.append("")
    lines.append("> SSOT: `.omo/_truth/registry/governance-checks.yaml::gac.rules`")
    lines.append("> Full generated digest: `docs/generated/agent-gac-rules.md`")
    lines.append("> Validate: `python3 bin/gac-validate.py --gate` | Drift: `python3 bin/gac-drift.py`")
    lines.append("> Regenerate: `python3 bin/gac-export-agents.py`")
    lines.append("")
    lines.append("Do not paste the full rule inventory into `AGENTS.md`; keep this file as an operational pointer.")
    lines.append("")
    lines.append(END_MARKER)
    return "\n".join(lines)


def update_generated_digest(gac_digest: str) -> None:
    GENERATED_DIR.mkdir(parents=True, exist_ok=True)
    GENERATED_RULES_MD.write_text(gac_digest + "\n", encoding="utf-8")
    print(f"✅ Updated {GENERATED_RULES_MD.relative_to(WORKSPACE)}")


def update_agents_md(gac_section: str) -> None:
    content = AGENTS_MD.read_text()

    if START_MARKER in content and END_MARKER in content:
        # Replace existing section
        start_idx = content.index(START_MARKER)
        end_idx = content.index(END_MARKER) + len(END_MARKER)
        new_content = content[:start_idx] + gac_section + content[end_idx:]
    else:
        # Append at end
        new_content = content.rstrip() + "\n\n" + gac_section + "\n"

    AGENTS_MD.write_text(new_content, encoding="utf-8")
    print(f"✅ Updated {AGENTS_MD.name} with GaC pointer")


def main():
    rules = load_rules()
    if not rules:
        print("❌ No GaC rules found in registry", file=sys.stderr)
        sys.exit(1)

    print(f"Loaded {len(rules)} GaC rules from registry")
    gac_digest = export_full_markdown(rules)
    gac_pointer = export_agents_pointer()

    if "--stdout" in sys.argv:
        print(gac_digest if "--full" in sys.argv else gac_pointer)
    else:
        update_generated_digest(gac_digest)
        update_agents_md(gac_pointer)


if __name__ == "__main__":
    main()

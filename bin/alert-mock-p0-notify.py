"""P72 R3: P0 mock 通知工具.

AUTO-GENERATED by post-commit knowledge extraction; review before major edits.

P0 触发时模拟"短信/邮件"通知 (本地文件, 无外部依赖):
- 写 runtime/logs/p0-notifications.log
- 模拟多通道: email / sms / slack
- 实际生产需替换为真实 SMS/email provider

使用:
  python3 bin/alert-mock-p0-notify.py --level P0 --message "..." --channel email
  python3 bin/alert-mock-p0-notify.py --dry-run
"""

from __future__ import annotations

import argparse
import json
import sys
from datetime import datetime, timezone
from pathlib import Path


CHANNELS = ["email", "sms", "slack"]


def send_mock(root: Path, level: str, message: str, channels: list[str], dry_run: bool = False) -> dict:
    """Mock 发送 P0 通知."""
    timestamp = datetime.now(timezone.utc).isoformat()
    notif_log = root / "runtime" / "logs" / "p0-notifications.log"
    results = {}
    for channel in channels:
        # 模拟 channel 特定输出
        if channel == "email":
            target = "oncall@omostation.local"
        elif channel == "sms":
            target = "+86-xxx-xxxx-xxxx"
        elif channel == "slack":
            target = "#governance-alerts"
        else:
            target = "unknown"

        payload = {
            "timestamp": timestamp,
            "level": level,
            "message": message,
            "channel": channel,
            "target": target,
            "delivered": not dry_run,
        }
        results[channel] = payload

        # 写日志
        if not dry_run:
            notif_log.parent.mkdir(parents=True, exist_ok=True)
            with open(notif_log, "a", encoding="utf-8") as f:
                f.write(json.dumps(payload, ensure_ascii=False) + "\n")
    return results


def main() -> int:
    parser = argparse.ArgumentParser(
        description="P72: P0 mock 通知工具 (本地文件, 无外部依赖)"
    )
    parser.add_argument("root", nargs="?", default=".", help="workspace root")
    parser.add_argument("--level", default="P0", help="告警级别 (默认 P0)")
    parser.add_argument("--message", required=True, help="告警消息")
    parser.add_argument("--channel", choices=CHANNELS, default="email", help="通知通道")
    parser.add_argument("--all-channels", action="store_true", help="所有通道")
    parser.add_argument("--dry-run", action="store_true", help="dry-run, 不写日志")
    args = parser.parse_args()

    root = Path(args.root).resolve()
    if not (root / ".omo").exists():
        print(f"❌ .omo/ not found at {root}")
        return 1

    channels = CHANNELS if args.all_channels else [args.channel]
    results = send_mock(root, args.level, args.message, channels, args.dry_run)

    print("=" * 60)
    print(f"📤 P72 P0 mock 通知")
    print("=" * 60)
    print(f"  Level: {args.level}")
    print(f"  Message: {args.message}")
    print(f"  Channels: {', '.join(channels)}")
    if args.dry_run:
        print("  Mode: dry-run (no log written)")
    print()
    for ch, p in results.items():
        status = "✅ delivered" if p["delivered"] else "⏸️  dry-run"
        print(f"  [{ch:<6s}] → {p['target']:<40s} {status}")
    if not args.dry_run:
        print()
        print(f"📁 Log: runtime/logs/p0-notifications.log")
    return 0


if __name__ == "__main__":
    sys.exit(main())